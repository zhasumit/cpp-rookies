#include <iostream>
using namespace std;

int main()
{
        int x = 11, y = 7;

        cout << "x = " << x << "\ty = " << y << endl;
        /*  NOT operator
                y -> 00000111
                ~y = 11111000

                y -> 00000111
                    +       1
                ~y = 00001000 = -8(decimal)
                                 */
        cout << "~y     = " << ~y << endl;
        // ==============================================

        /*   Bitwise AND
                11 -> 00001011
                7  -> 00000111
                x&y = 00000011
         */
        cout << "x & y  = " << (x & y) << endl;
        // ==============================================

        /*   Bitwise OR
                11 -> 00001011
                7  -> 00000111
                x|y = 00001111
         */
        cout << "x | y  = " << (x | y) << endl;
        // ==============================================

        /*   Bitwise X-OR
                11 -> 00001011
                7  -> 00000111
                x^y = 00001100
         */
        cout << "x ^ y  = " << (x ^ y) << endl;
        // ==============================================

        /*   Leftshift
                11   = 00001011
                x<<1 = 00010110 => 11*(2^1) = 22(Decimal)
         */
        cout << "x << 1 = " << (x << 1) << endl;
        // ==============================================

        /*   Rightshift
                11   = 00001011
                x>>1 = 00000010 => 11/(2^2) = 2(Decimal)
         */
        cout << "x >> 2 = " << (x >> 2) << endl;
        // ==============================================

        return 0;
}